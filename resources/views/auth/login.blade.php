    <script>
        function toggleForm(e) {
            e.preventDefault();

            const loginCard    = document.querySelector('.login-card');
            const registerCard = document.querySelector('.register-card');
            const anchor1      = document.querySelector('.anchor1');
            const anchor2      = document.querySelector('.anchor2');
            const heading      = document.getElementById('panel-heading');
            const sub          = document.getElementById('panel-sub');

            // check current state by class, not style.display
            const isLogin = !loginCard.classList.contains('hidden');

            if (isLogin) {
                // fade out login → fade in register
                loginCard.classList.add('hidden');
                registerCard.classList.remove('hidden');
                anchor1.classList.add('hidden');
                anchor2.classList.remove('hidden');
                heading.textContent = 'Create account';
                sub.textContent     = 'Sign up to get started';
            } else {
                // fade out register → fade in login
                registerCard.classList.add('hidden');
                loginCard.classList.remove('hidden');
                anchor2.classList.add('hidden');
                anchor1.classList.remove('hidden');
                heading.textContent = 'Welcome back';
                sub.textContent     = 'Log in to access your account';
            }
        }
    </script>

